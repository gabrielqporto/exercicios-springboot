package br.com.cod3r.exerciciossb.models.entities;

public class Calculadora {
	
	public int a;
	public int b;
	
	public Calculadora(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	
	
	
	
}
